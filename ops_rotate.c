#include "push_swap.h"

void	ops_rotate(t_stack **stck, int f)
{
	if (*stck)
	{
		*stck = (*stck)->next;
		if (f == 1)
			write(1, "ra\n", 3);
		else if (f == 2)
			write(1, "rb\n", 3);
	}
}